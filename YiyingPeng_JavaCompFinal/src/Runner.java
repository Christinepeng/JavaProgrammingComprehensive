class Runner {
    private String runnersName;
    private int runnersSpeed;
    private int restPercentage;

    String getRunnersName() {
        return runnersName;
    }

    void setRunnersName(String runnersName) {
        this.runnersName = runnersName;
    }

    int getRunnersSpeed() {
        return runnersSpeed;
    }

    void setRunnersSpeed(int runnersSpeed) {
        this.runnersSpeed = runnersSpeed;
    }

    int getRestPercentage() {
        return restPercentage;
    }

    void setRestPercentage(int restPercentage) {
        this.restPercentage = restPercentage;
    }
}
